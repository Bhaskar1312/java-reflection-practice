package methodDiscovery.api;

import javafx.css.Size;

public class ClothingProduct extends Product {
    private Size size;
    private String color;

    // Getters
    public Size getSize() {
        return size;
    }

    public void setSize(Size size) {
        this.size = size;
    }

    // Setters

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
